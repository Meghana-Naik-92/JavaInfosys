package april14;

public class Relational {

    public static void main(String args[]) {
        int numOne = 10;
        int numTwo = 5;
        System.out.println(numOne > numTwo);//Output will be true
        System.out.println(numOne < numTwo);//Output will be false
        System.out.println(numOne >= numTwo); //Output will be true
        System.out.println(numOne <= numTwo);//Output will be false
        System.out.println(numOne == numTwo);//Output will be false
        System.out.println(numOne != numTwo);//Output will be true
    }

}
